package Spoj_Solution.com;

import java.util.Scanner;

public class SMPWOW {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        int n=sc.nextInt();
        System.out.print("W");
        for (int i=0;i<n;i++){
            System.out.print("o");
        }
        System.out.println("w");
    }
}
